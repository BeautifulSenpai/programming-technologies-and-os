"""
Задача 4. Индикатор загрузки (flush)
Уровень: Средний
Проверяемые навыки: Циклы, параметры end и flush, управление кареткой (\r), time.sleep().
Задание:
Создайте имитацию индикатора загрузки в одной строке. Программа должна:
1. Выводить сообщение Загрузка: [.
2. Затем в течение 5 секунд постепенно заполнять полоску символами #. Каждую секунду должен добавляться один символ #. Полоска должна быть длиной 10 символов
3. В конце полоски должны быть символы ] и проценты.
4. Когда полоска заполнится (через 5 секунд), строка должна измениться на Загрузка:
[##########] 100% и курсор должен перейти на новую строку.
Визуально это должно выглядеть как анимированный прогресс-бар, который обновляется на месте.
Ожидаемая анимация (по секундам):
• Секунда 0: Загрузка: []0%
• Секунда 1: Загрузка: [# ] 10%|
• Секунда 2: Загрузка: (## ] 20%
....
• Секунда 10: Загрузка: [##########]100% (и перевод строки)
Подсказка:
• Используйте символ \r (возврат каретки) в начале строки, чтобы каждый раз печатать заново поверх предыдущей строки.
• Комбинируйте end='' и flush=True.
• Для паузы используйте time.sleep(1)
"""

import time

for i in range(11):
    filled = '#' * i
    percent = i * 10
    print(f"\rЗагрузка: [{filled}] {percent}%", end='', flush=True)
    if i < 10:
        time.sleep(1)

print()