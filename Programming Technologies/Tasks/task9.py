"""
Напишите программу, которая запрашивает у пользователя процент выполнения тестов(целое число от 0 до 100) и выводит буквунную оценку по следующей шкале:
90-100 - А (Отлично)
90-100 - B (Хорошо)
90-100 - C (Удовлетворительно)
90-100 - D (Неудовлетворительно)
90-100 - F (Провал)
Если число вне диапазона 0-100, вывести сообщение об ошибке
"""

grades = {
    (90, 100): "A (Отлично)",
    (80, 89): "B (Хорошо)",
    (70, 79): "C (Удовлетворительно)",
    (60, 69): "D (Неудовлетворительно)",
    (0, 59): "F (Провал)"
}

grade_ranges = list(grades.keys())

try:
    score = int(input("Введите процент выполнения тестов (0-100): "))
    
    if 0 <= score <= 100:
        for min_score, max_score in grade_ranges:
            if min_score <= score <= max_score:
                print(f"Ваша оценка: {grades[(min_score, max_score)]}")
                break
    else:
        print("Ошибка: число должно быть в диапазоне от 0 до 100!")
        
except ValueError:
    print("Ошибка: пожалуйста, введите целое число!")