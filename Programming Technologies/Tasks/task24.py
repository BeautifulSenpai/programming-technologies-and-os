"""
Задача 9. Сортировка таблицы по нескольким столбцам
Дан список table, где каждая строка — это список значений: [название,
цена, рейтинг]. Отсортируйте эту таблицу по следующим правилам:
- сначала по возрастанию цены (второй элемент),
- при равной цене — по убыванию рейтинга (третий элемент),
- при равном рейтинге — по алфавиту названия (первый элемент).
Результат сохраните в переменной sorted_table. Используйте
возможности параметра key метода sort() или функции sorted() с передачей
кортежа ключей.
table = [
 ["B", 100, 4.5],
 ["A", 100, 4.5],
 ["C", 50, 5.0],
 ["D", 100, 5.0],
]
print(sorted_table)
# Ожидаемый порядок:
# ['C', 50, 5.0]
# ['D', 100, 5.0]
# ['A', 100, 4.5]
# ['B', 100, 4.5]
"""

table = [
    ["B", 100, 4.5],
    ["A", 100, 4.5],
    ["C", 50, 5.0],
    ["D", 100, 5.0],
]

sorted_table = sorted(table, key=lambda row: (row[1], -row[2], row[0]))

for row in sorted_table:
    print(f"# {row}")