"""
Условие:
Напишите программу, которая запрашивает у пользователя неотрицательное целое число и вычисляет его факториал.
Факториал числа n (обозначается n!) — это произведение всех целых чисел от 1 до n. Для 0 факториал равен 1.
Программа должна:
1. Если введено отрицательное число — вывести "Ошибка: факториал определен только для неотрицательных чисел"
2. Если введено число больше 10 — вывести "Ошибка: программа может вычислить факториал только для чисел от 0 до 10"
3. Для чисел от 0 до 10 — вычислить и вывести результат
"""
while True:
    try:
        n = int(input("Введите неотрицательное целое число: "))
        
        if n < 0:
            print("Ошибка: факториал определен только для неотрицательных чисел")
        elif n > 10:
            print("Ошибка: программа может вычислить факториал только для чисел от 0 до 10")
        else:
            factorial = 1
            for i in range(1, n + 1):
                factorial *= i
            print(factorial)
            break
            
    except ValueError:
        print("Ошибка: введите корректное целое число")